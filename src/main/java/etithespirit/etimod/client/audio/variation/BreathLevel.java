package etithespirit.etimod.client.audio.variation;

/**
 * Represents the depth of a breath after emerging from water.
 * @author Eti
 *
 */
public enum BreathLevel {
	/** You've only been under for a few seconds. */
	LITTLE,
	
	/** You've been under for a little while, and are sort of exhausted. */
	MEDIUM,
	
	/** You've been under for a F A T minute, and are very exhausted. */
	BIG
}